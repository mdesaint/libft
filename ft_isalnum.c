#include <stdio.h>
#include <ctype.h>
#include "libft.h"

int	ft_isalnum(int str)
{
	if ((str >= 48 && str <= 57) || (str >= 65 && str <= 90)
		|| (str >= 97 && str <= 122))
		return (1);
	else
		return (0);
}
